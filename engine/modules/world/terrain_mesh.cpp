static u64 dom_mesh_hash_u8(u64 h, u8 v)
{
    h ^= (u64)v;
    h *= 1099511628211ULL;
    return h;
}

static u64 dom_mesh_hash_u32(u64 h, u32 v)
{
    h = dom_mesh_hash_u8(h, (u8)(v & 0xFFu));
    h = dom_mesh_hash_u8(h, (u8)((v >> 8u) & 0xFFu));
    h = dom_mesh_hash_u8(h, (u8)((v >> 16u) & 0xFFu));
    h = dom_mesh_hash_u8(h, (u8)((v >> 24u) & 0xFFu));
    return h;
}

static q16_16 dom_mesh_clamp_q16_16(q16_16 v, q16_16 lo, q16_16 hi)
{
    if (v < lo) return lo;
    if (v > hi) return hi;
    return v;
}

static dom_domain_point dom_mesh_interp(const dom_domain_point* p1,
                                        const dom_domain_point* p2,
                                        q16_16 valp1,
                                        q16_16 valp2)
{
    dom_domain_point out;
    q16_16 denom = d_q16_16_sub(valp1, valp2);
    q16_16 t;
    if (denom == 0) {
        out.x = (q16_16)(((i64)p1->x + (i64)p2->x) >> 1);
        out.y = (q16_16)(((i64)p1->y + (i64)p2->y) >> 1);
        out.z = (q16_16)(((i64)p1->z + (i64)p2->z) >> 1);
        return out;
    }
    t = d_fixed_div_q16_16(valp1, denom);
    t = dom_mesh_clamp_q16_16(t, 0, d_q16_16_from_int(1));
    out.x = d_q16_16_add(p1->x, d_q16_16_mul(d_q16_16_sub(p2->x, p1->x), t));
    out.y = d_q16_16_add(p1->y, d_q16_16_mul(d_q16_16_sub(p2->y, p1->y), t));
    out.z = d_q16_16_add(p1->z, d_q16_16_mul(d_q16_16_sub(p2->z, p1->z), t));
    return out;
}

